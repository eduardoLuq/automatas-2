    class DToken {
        
        private int no;
        private String tok;
        private String tipoTok;

    public DToken(int no, String tok, String tipoTok) {
        this.no = no;
        this.tok = tok;
        this.tipoTok = tipoTok;
    }

    public int getNo() {
        return no;
    }

    public String getTok() {
        return tok;
    }

    public String getTipoTok() {
        return tipoTok;
    }

        
        
        
    }


package ArbolSintactico;

public class Declarax {
    public Typex s1;
    public String s2;    
    
    public Declarax(Typex st1, String st2) {
        s1 = st1;
        s2 = st2;
    }

    public Typex getTypex() {
        return s1;
    }

    public String getString() {
        return s2;
    }
}
